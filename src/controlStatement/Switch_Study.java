package controlStatement;

public class Switch_Study {

	public static void main(String[] args) {
		int year=3;
		switch (year) {
		case 1:System.out.println("Welcome to the 1st yr");
			
		break;
			
		
		case 2:System.out.println("Welcome to the 2nd yr");
				
		break;
		
		case 3:System.out.println("Welcome to the 3rd yr");
					
		break;

		case 4:System.out.println("Welcome to the 4th yr");
						
		break;
		
		case 5:System.out.println("Welcome to the 5th yr");
							
		break;
		 
		case 6:System.out.println("Welcome to the 6th yr");
								
		break;
		

		default:System.out.println("Plese enter values between 1 to 6");
			break;}
		}
		
}

	

