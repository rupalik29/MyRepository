package loopstudy;

public class for_loop_study {

	public static void main(String[] args) {
		
		for (int a=5;a<=50;a=a+5) {
			System.out.println(a);
		}
		System.out.println("====================================================");
		for (int z=50;z>=5;z=z-5) {
			System.out.println(z);
		}
		System.out.println("===================================");
		for(int a=1;a<=10;a++)
		{
			System.out.println("Roopali");
		}
		System.out.println("===============================");
		for (int a=10;a>=1;a--) {
			System.out.println(a);
		}System.out.println("================================");
		for(int i=6;i<=60;i=i+6) {
			System.out.println(i);
		}
		for(int k=40;k>=4;k=k-4) {
			System.out.println(k);
		}
		for(int p=3;p<=30;p++) {
			System.out.println(p);
		}
		
		
		
		
	}
	
	
	
	
	
	
	
	
	
}
