package loopstudy;

public class while_study {

	public static void main(String[] args) {

		int i=3;
		while(i<=30) {
			System.out.println(i);
			i=i+3;
		}
		System.out.println("============================");
		int z=20;
		while(z>=2) {
			System.out.println(z);
			z=z-2;
		}
		System.out.println("==================================================");
		
		int a=5;
		while(a<=50) {
			System.out.println(a);
			a=a+5;}
		}

}
